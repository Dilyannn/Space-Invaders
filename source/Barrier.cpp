Block::Block(Vector2 position): position(position) {}

void Block::render() {
    DrawRectangle(position.x, position.y, 6, 6, RED);
}

Rectangle Block::getRect() {
    Rectangle rect;
    rect.x = position.x;
    rect.y = position.y;
    rect.width = 3;
    rect.height = 3;
    return rect;
}

